#include <stm32f4xx.h>
#include <system_cx100.h>
#include "debug.h"

// ============================================================================
// Public variables
// ============================================================================
uint32_t DEBUG_Counter = 0;

// ============================================================================
// Private variables
// ============================================================================

// ============================================================================
// Function prototypes
// ============================================================================

// ----------------------------------------------------------------------------
// DEBUG_TimerStart()
// -----------------
// Start debug timer
// ----------------------------------------------------------------------------
void DEBUG_TimerStart(void)
{
	SYSTEM_DEBUG_TIMER->EGR |= TIM_EGR_UG;
	SYSTEM_DEBUG_TIMER->CR1 |= TIM_CR1_CEN;
}

// ----------------------------------------------------------------------------
// DEBUG_TimerStop()
// -----------------
// Stop debug timer and assign the value to the counter
// ----------------------------------------------------------------------------
void DEBUG_TimerStop(void)
{
	// Stop the timer
	SYSTEM_DEBUG_TIMER->CR1 &= !TIM_CR1_CEN;

	// Save counter
	DEBUG_Counter = SYSTEM_DEBUG_TIMER->CNT;

	// Reset timer counter
	SYSTEM_DEBUG_TIMER->CNT = 0;
}
